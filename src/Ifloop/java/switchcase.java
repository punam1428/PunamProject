package Ifloop.java;

public class switchcase {

	public static void main(String[] args) {
        int a = 2;

		switch(a) {
		
		case 0:
			System.out.println("zero");
		    break;
		case 1:
			System.out.println("one");
		    break;
		case 2:
			System.out.println("two");
		    break;
		default:
			System.out.println("not valid");
		}

	}

}
