package Ifloop.java;

public class elseif {

	public static void main(String[] args) {
		int a = 0;

		
		
		if(a==0) {
			System.out.println("A is equal to 0");
 
		}
		
		else if(a>0) {
			System.out.println("A is greater than 0");
 
		}
		
		else {
			System.out.println("A is less than 0");
		}
		

	}

}
