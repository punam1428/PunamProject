package Ifloop.java;

public class elseif2 {

	public static void main(String[] args) {
        int a = 3;

		
		
		if(a==0) {
			System.out.println("zero");
 
		}
		
		else if(a==1) {
			System.out.println("one");
 
		}
		
		else if(a==2){
			System.out.println("two");
		}
		else {
			
			System.out.println("not valid");
		}

	}

}
